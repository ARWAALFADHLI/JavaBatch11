package whileLoopReviewofYT;

public class WhileLoop {

	public static void main(String[] args) {
		
		
		int a=0;
		
		while(a<10) { // a while loop repeats code forever until a trigger you specify happens
			          // only accepts the true value and never the false
			System.out.println("a is less than 10: "+a);
			a++;
			
		}
		
	}

}
