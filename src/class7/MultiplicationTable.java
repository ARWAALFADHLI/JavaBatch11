package class7;

public class MultiplicationTable {

	public static void main(String[] args) {
		int num=9;
		for(int a=1; a<=10; a++) {
			System.out.println(num+ "x" + a+ "=" + num*a);
		}
		System.out.println();
		System.out.println("***************");
		for(int b=1; b<=20; b+=2) {
			System.out.print(b+" ");
		}
		}

}
